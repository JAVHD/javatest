public class Construct {

	private String name;
	private Integer age;

	public Construct(String name) {
		this.name = name;
	}

	public Construct(Integer age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public Integer getAge() {
		return age;
	}


}